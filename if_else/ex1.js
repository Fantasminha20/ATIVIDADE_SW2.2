export function resultadoIfElse(numero) {
  // let numero = -6


  if (numero > 0) {
    return "seu numero é positivo"
  }
  if (numero < 0) {
    return "seu número é negativo"
  }
  if (numero == 0) {
    return "seu numero é 0"
  }

};


